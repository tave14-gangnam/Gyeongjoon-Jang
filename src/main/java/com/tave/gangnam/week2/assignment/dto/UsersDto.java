package com.tave.gangnam.week2.assignment.dto;

public class UsersDto {

    private Long id;

    private String name;

    private int age;

    public UsersDto() {}

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
